                <h2>My Impression of Playstaion VR</h2>
                <p>I guess my first experience of VR was back some time in the early 90's.  I remember a Game or maybe Electronics Boutique shop in the Shires shopping centre in Leicester had this huge big... thing... you would stand inside and put the VR helmet on. In the game I remember playing you drove a tank round and tried to blow up the other tanks.  The problem was, back then we just didn't have the processing power.  The lag was a good half a second or so, which rather broke the immersion (if the rediculous big block graphics already hadn\'t) and also made you feel rather ill.</p>
                <p>These big units didn't seem to last long and then vanished again not to be seen for a good 20 years or so.  I think I first had my own VR headset around 3 years ago thanks to Samsung and their Gear headset.  How crazy!  We went from having a huge big unit you stood inside, which couldn't cope with the task of VR, to having it in our phones!</p>
                <p>Whilst it's really very impressive what can be done on your phone with the addition of a headset, there are certain limitations and I've long wanted a more specialised VR unit. There are a few on the market at the moment, Oculous Rift and the HTC Vive being perhaps the best known options, but both of these are £400+ and also require a pretty full on gaming PC to make use of them.  Then along came Sony with their PS4 Pro and they released a PS VR headset, which can now be had for just £200!  I had to give it a try!</p>
                <img src="../../images/benq1.jpg" width="100%" alt="BenQ ScreenBar" width="1100"/>
                <p>I have to say, all of them are quite useful, bar maybe the colour temperature.  In my time using it, I've not really moved that about much, but you can change the colour from a pure white, through to a warm yellow.  I prefer the white end of things myself.</p>
