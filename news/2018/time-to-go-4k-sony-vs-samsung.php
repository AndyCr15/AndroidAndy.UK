                <h6 class="newsdate">25th May 2018</h6>
                <h2>Time to go 4K - Sony vs Samsung</h2>
                <p>Up until very recently I thought it was too expensive to go 4K.  I remember last year I noticed some quite nice 4K TV's for around £600, but decided against it as there still wasn't much 4K content available.  I'm not sure what really changed my mind this month, but I upgraded both our living room TV (55") and my bedroom TV (40") to 4K HDR and wow... amazing!  It's not even the 4K content, the 1080p I watch looks outstanding!  I now understand what people mean when they say it looks too real.  It's hard to explain, but every now and then you get a shot that just looks so real, you expect the director to walk in shouting 'cut!'.</p>
                <img src="../../images/samsung4k.jpg" width="100%">
                <h4>Samsung</h4>
                <p>Initially I went Samsung for both TV's.  In the living room we went with a <a href="https://www.johnlewis.com/samsung-qe55q6f-qled-hdr-1000-4k-ultra-hd-smart-tv-55-inch-with-tvplus-freesat-hd-360-design-ultra-hd-certified-black/p3336290">Samsung QE55Q6F QLED HDR 55"</a> monster, which looks fantastic.  The images look amazing and the screen is so bright and vibrant.  Throw in that it's almost got no bezel and I love this TV.  The OS is Samsungs own 'Samsung Smart TV' which I have to say is very smooth and slick.</p>
                <p>In my room I had a <a href="https://www.johnlewis.com/samsung-ue40mu6400-hdr-4k-ultra-hd-smart-tv-40-inch-with-tvplus-freesat-hd-active-crystal-colour-silver/p3204464">Samsung UE40MU6400</a> 40" screen.  I had a small issue in that I find TV manufacturers don't include the bolts needed to mount the TV to the wall.  Really?!  It would cost them pennies.  It also wasn't too clear which bolts I needed, so if you buy one, I can tell you M8 45mm worked great for me.</p>
                <p>The image of the 40" can't quite match up with the 55", but still looks fantastic.  I was inititally worried as there was no headphone jack, which I used to plug my Bluetooth transmitter into, nor could my optical audio cable reach from the SoundBar.  Thankfully, I then realised the TV itself had Bluetooth!  Game changer!  I could now pair Bluetooth audio devices, like multiple different headphones and my soundbar without the need for cables.  Neither did I still need my Chromecast or Amazon Fire Stick and thanks to it also having WiFi, literally all I had plugged into the TV was the power.</p>
                <p>Unforetunately, very quickly I realised I had an issue.  The TV would randomly restart itself.  Then one night it went into a boot loop.  All I could do was pull the power and go watch the TV in the living room instead.  Thankfully I called John Lewis and they agreed to replace it for me, but I decided to replace it with something differenty</p>
                <h4>Sony</h4>
                <p>It was about £150 more expensive, but I treated myself to a <a href="https://www.johnlewis.com/sony-bravia-kd43xe8005-led-hdr-4k-ultra-hd-smart-android-tv-43-inch-with-freeview-hd-youview-black/p3167623">Sony Bravia KD43XE8005</a>.  Knowning the issue I had with bolts for the wall mounnt of the Samsung, I thought I'd get ahead of this one.  I ordered some M6 15mm that literally came through my letterbox 30 minutes before the TV was delivered.</p>
                <img src="../../images/sony4k.jpg" width="100%">
                <p>The image looks to be just as good as the Samsung, with the added bonus that it's running Android TV.  This made set up a breeze.  I said I have an Android phone and it basically set itself up through my phone!  I also hadn't thought about it until I had it, but this meant I could install/sideload all kinds of media apps.  Not many are in the Play store, but you can install a file manager like <a href="https://play.google.com/store/apps/details?id=com.lonelycatgames.Xplore&hl=en_GB">X-Plore</a> and then upload apk's to Dropbox and install them from there.  I very quickly had 'Whose Line Is It Anyway' channel streaming through Mobdro.</p>
                <p>I also got a couple of 4K Movies through Google Play thrown in too, I guess worth around £18.  It's not any movies, but the selection was from around 20 or 30 titles. (I chose the new Bladerunner and the new Jumanji)</p>
                <p>It's not all roses though.  Strangly I now find that it doesn't have A2DP!  This means although it has Bluetooth, it can't stream the sound to audio devices over it.  Rather annoying.  Back out come the cables!  It's not the end of the world though, the optical audio cable reaches and it's actually easier connecting to my transmitter than going into the TV settings each time.</p>
                <p>Then other downside is actually Android TV.  Although I love what it can allow, it seems a little clunky and awkward.  I have to admit Samsung Smart TV was quicker and smoother.  For example, on the Samsung go to 'sources' and there's my DLNA Server, waiting to be selected.  On the Sony I have to open an app, such as VLC and browse to the DLNA server.  Not a big issue, but just seemed easier on the Samsung.</p>
                <p>So, I guess the big question is, which is better?  Well, they're both great and (provided your Samsung works) you wouldn't be unhappy with either.  I guess it then folds down to how much you would want to install less than kosher apps on your TV.  If that's important to you, the Sony is the obvious choice.  If not, it could be the Samsung is a better buy at £150 or so less.</p>
