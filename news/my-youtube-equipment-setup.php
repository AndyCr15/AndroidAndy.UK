                <h2>My YouTube Equipment Setup</h2>
                <p>Every now and then I get asked about what equipment I use to make my YouTube videos. Well, once I got asked.  Okay, once I told someone and now I'm going to tell you...</p>
                <p>It doesn't take a huge amount of money to get a decent setup, but you do need to spend a little.  If I get to a YouTube video and the lighting means the camera can't focus or the person can barley be understood as the audio is that bad, I quite often just move on.  I think my setuo can be had for around £500, which I think is reasonable if you're serious about YouTube videos. So, lets take a look at the kit.</p>
                <img src="../images/youtubesetup.jpg" width="100%">
                <p>1. Canon S110 - This is actually a relatively old model now, Amazon tells me it was first available in Sept 2012!  Even the S200 is no longer available new.  I really should upgrade, but I'm waiting for 4K Compact cameras to become an affordable option.</p>
                <p>2. <a href="https://amzn.to/2EQmJXB">Lighting</a> - One of the most overlooked aspects when you see 'amatuer' videos on YouTube.  For only around £60 you can get free standing lamps that make all the difference to your videos.  With low lighting the camera can really struggle to focus, as well as the noise that will creep in making the overall image quality much worse.  Spend the money and have far better looking videos in an instant. You usually get reflective umrellas with the lights too.  If you have the space, great, they soften shadows.  For me, I don't have the room so I don't use them.</p>
                <p>3. <a href="https://amzn.to/2HCNOAx">Rode NT1</a> - I think audio is only really one of those things you notice when it's not good.  This Rode is a fantastic mic enabling me to move about and still sound clear on my videos.  You don't have to spend hundreds though with mics like the <a href="https://amzn.to/2JUG79O">Blue Snowball</a> being quite popular among YouTubers for only £45.</p>
                <p>4. <a href="https://amzn.to/2HB97CF">Focusrite Scarlett Solo Audio Interface</a> - For my microphne, this is needed.  It then plugs into a USB port on my PC. I did have a slightly cheaper AKG mic before, which would go directly into the mic socket on the PC, but the mic had to be right up to my mouth for the best audio.  With this setup, there is little audio drop off as I move around, which is quite important.</p>
                <p>5. Camera stand - Don't just hold your camera with one hand while unboxing with the other... please!  For only a few £££ you can get a half decent stand. I actually don't know where I bought the one pictured and can't find a similar one on Amazon.  Someone <a href="../contact.php">let me know</a> if you know where it can be bought and I'll add it in.  I prefer it over a small tripod as I can easily reach round it to operate the phone.</p>
                <p>6. <a href="https://amzn.to/2qCxcRp">Logitech C920 Webcam</a> - This is of course very much optional if you're setting up yourself and a relativelty recent feature to my videos.  It was commented by a few they like the videos I'm actually in myself and sometimes I like to just talk to camera, so for only around £60 I thought it a good addition.</p>
                <p>7. <a href="">Background</a> - Not pictured here, but I made a background from wood and coloured felt as the black background of my desk meant when the phone screen was on camera, it blew everything out and the camera really struggled to make out the bright and dark contrasts.  When filming, remember to turn your phone screen brioghtness down too.</p>
